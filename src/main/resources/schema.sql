
CREATE TABLE IF NOT EXISTS clients (
  id INT GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
  first_name  VARCHAR(25) NOT NULL,
  last_name   VARCHAR(40) NOT NULL,
  phone       VARCHAR(40) NOT NULL,
  email       VARCHAR(50) NOT NULL,
  passport    VARCHAR(40) NOT NULL
 );

 CREATE TABLE  IF NOT EXISTS credits (
  id INT GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
  name            VARCHAR(25) NOT NULL,
  credit_limit    NUMERIC(19, 2) NOT NULL,
  interest_rate   NUMERIC(19, 2) NOT NULL,
 );


CREATE TABLE  IF NOT EXISTS credit_offers (
  id INT GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
  client_id  INT REFERENCES clients(id),
  credit_id  INT REFERENCES credits(id),
  loan_amount DOUBLE,
  loan_term_year DOUBLE
--  start_date DATE
 );
